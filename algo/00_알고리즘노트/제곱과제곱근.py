# 제곱
import math

# math.pow 반환 타입 float
print(type(math.pow(10, 20)))

result1 = math.pow(2, 4)
print("math.pow(2, 4) : " + str(result1))

result2 = math.pow(1, 5)
print(f"math.pow(1, 5) : {result2}")

result3 = math.pow(9, 0)
print(f"math.pow(9, 0) : {result3}")

