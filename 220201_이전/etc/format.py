num1 = 1
num2 = 3
print("{0}나누기 {1}은 {2}이다.".format(num1, num2, num1 / num2))
# 1나누기 3은 0.3333333333333333이다.

print("{0}나누기 {1}은 {2:.2f}이다.".format(num1, num2, num1 / num2))
print("{}나누기 {}은 {:.2f}이다.".format(num1, num2, num1 / num2))
# 1나누기 3은 0.33이다.